#Operaciones Aritmeticas
grade1 = 10
grade2 = 11
bonus = 3
grade1 += bonus
sum = grade1 + grade2
avg = sum / 2
print("El promedio es:", avg)

#Division entera
avg_int = sum // 2
print("Parte entera del promedio:", avg_int)

#Potencia
base = 2
exponent = 3
print(f"{base} elevado a la potencia {exponent} es {base ** exponent}")